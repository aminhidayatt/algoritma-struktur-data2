public class node {
	
	private String label;
	private int value;
	//method setter
	public void setlabel(String label){
		this.label=label;
	}
	public void setvalue(int value){
		this.value=value;
	}
	//method getter
	public String getlabel(){
		return label;
	}
	public int getvalue(){
		return value;
	}
	
	

}
